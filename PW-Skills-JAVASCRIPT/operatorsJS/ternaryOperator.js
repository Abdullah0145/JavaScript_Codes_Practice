//Ternary Operator / Ternary Condition
// condition ? TRUE:FALSE
//Ternary Operator/Ternary Condition: ?(? means True), :(: means False)
isloggedIn = true;
isloggedIn ? console.log("HOME PAGE"): console.log("LoginPage");

isloggedIn = false;
isloggedIn ? console.log("HOME PAGE"): console.log("LoginPage");