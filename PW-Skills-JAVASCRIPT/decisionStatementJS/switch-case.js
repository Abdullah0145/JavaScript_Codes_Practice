//Switch Case of alternative if/else
let user = "Admin"
switch (user) {
    case "Admin":
        console.log("He is Admin")
        break

    case "Student":
        console.log("He is Student")
        break

    case "Mentors":
        console.log("He is Mentor");  
        break
    
    default:
        console.log("I am default");
}
